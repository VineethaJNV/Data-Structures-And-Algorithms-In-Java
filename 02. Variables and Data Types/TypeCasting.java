public class TypeCasting {
    public static void main(String[] args){
        float num = 99.0f;
        int marks = (int) num;
        System.out.println(marks);

        char ch = 'v';
        int num1 =(int) ch;
        System.out.println(num1);

        double n = 100.08;
        int n1 = (int)n;
        System.out.println(n1);
    }
}
