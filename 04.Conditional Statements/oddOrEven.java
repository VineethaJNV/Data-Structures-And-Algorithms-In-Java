public class oddOrEven {
    public static void main(String[] args){
       int num = 1;
       
       if(num % 2 == 0){
        System.out.println("even number");
       }if(num % 2 == 1){
        System.out.println("odd number");
       }else{
        System.out.println("");
       }
    }
}
